# Sistema de regulación y validación de jugadores
st.header("5. Sistema de Regulación y Validación de Jugadores")
st.markdown("""
🔒 **Validación de Ofertas y Jugadores:**  
TransferbyAgent contará con un **sistema de validación** que comprueba si el perfil de un jugador se ajusta a los requisitos de cada mercado y liga. Si el jugador tiene el potencial de competir en una liga de primer nivel, el sistema lo validará; si no, notificará al agente sobre las limitaciones del perfil del jugador.

⚠️ **Ejemplo de Regulación:**  
Un jugador de la liga ecuatoriana podría ser validado para mercados más pequeños o intermedios, pero si un agente intenta presentarlo a un club español, el sistema verificará si su perfil (goles, rendimiento, edad) cumple con los estándares exigidos por la liga española.

🔔 **Notificaciones Automáticas:**  
En caso de desajustes, el sistema generará **alertas automáticas** y ofrecerá **recomendaciones** sobre qué mejorar en el perfil del jugador o qué ligas serían más adecuadas.
""")

